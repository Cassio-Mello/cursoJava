package variaveis;
/*
 * Aula pratica sobre variaveis java
 */
public class VariaveisNotasEstudo {
    public static void main(String[] args) {
        //variaveis data atual
        int dia = 29;
        int mes = 04;
        int ano = 2023;

        //atribuir novo valor a variavel
        dia = 30;

        //impressao data atual
        System.out.println(dia);
        System.out.println(mes);
        System.out.println(ano);
    }
}
